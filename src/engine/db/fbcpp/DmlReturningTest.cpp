#include <iostream>
#include <string>
#include <vector>
#include <ctime>

#include <ibpp.h>

#include "engine/db/DatabaseFactory.h"
#include "engine/db/IDatabase.h"
#include "engine/db/ITransaction.h"
#include "engine/db/IStatement.h"

namespace
{

bool check(bool condition, const char* testName)
{
    if (condition)
    {
        std::cout << "  PASSED: " << testName << "\n";
        return true;
    }
    std::cerr << "  FAILED: " << testName << "\n";
    return false;
}

} // namespace

int main()
{
    const char* envServer = std::getenv("IBPP_TEST_SERVER");
    const std::string serverName = envServer ? envServer : "";
    if (serverName.empty())
    {
        std::cout << "IBPP_TEST_SERVER is not set, skipping DmlReturningTest.\n";
        return 0;
    }

    const std::string dbName = "/tmp/flamerobin_dml_returning_test_" +
        std::to_string(static_cast<long long>(std::time(0))) + ".fdb";

    // Create DB using IBPP
    try 
    {
        IBPP::Database db = IBPP::DatabaseFactory(serverName, dbName, "SYSDBA", "masterkey");
        db->Create(3);
    }
    catch (const IBPP::Exception& e)
    {
        std::cerr << "Failed to create test database: " << e.what() << "\n";
        return 1;
    }

    bool ok = true;
    std::cout << "Starting DML RETURNING tests...\n";

    try 
    {
        fr::IDatabasePtr db = fr::DatabaseFactory::createDatabase(fr::DatabaseBackend::FbCpp);
        db->setConnectionString(dbName);
        db->setCredentials("SYSDBA", "masterkey");
        db->connect();

        // Check version - multi-row RETURNING requires FB 5.0+
        std::string version = db->getEngineVersion();
        std::cout << "  Engine version: " << version << "\n";
        if (version.find("WI-V5.0") == std::string::npos && version.find("LI-V5.0") == std::string::npos)
        {
             // Try to parse ODS version if available, but for now we just check string
             // In CI we expect FB 5.0
        }

        fr::ITransactionPtr tr = db->createTransaction();
        tr->start();

        fr::IStatementPtr st = db->createStatement(tr);
        
        std::cout << "  Creating test table...\n";
        st->prepare("CREATE TABLE t1 (id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, val VARCHAR(20))");
        st->execute();
        
        std::cout << "  Inserting initial data...\n";
        st->prepare("INSERT INTO t1 (val) VALUES ('A')");
        st->execute();
        st->prepare("INSERT INTO t1 (val) VALUES ('B')");
        st->execute();

        // Test 1: Multiple-row INSERT ... RETURNING
        std::cout << "  Testing Multiple-row INSERT ... RETURNING...\n";
        st->prepare("INSERT INTO t1 (val) SELECT val || '2' FROM t1 RETURNING id, val");
        st->execute();
        
        ok = check(st->getColumnCount() == 2, "INSERT RETURNING column count") && ok;
        
        int rows = 0;
        while (st->fetch())
        {
            int id = st->getInt32(0);
            std::string val = st->getString(1);
            std::cout << "    Fetched: id=" << id << ", val=" << val << "\n";
            rows++;
        }
        ok = check(rows == 2, "INSERT RETURNING row count") && ok;

        // Test 2: Multiple-row UPDATE ... RETURNING
        std::cout << "  Testing Multiple-row UPDATE ... RETURNING...\n";
        st->prepare("UPDATE t1 SET val = val || '!' RETURNING id, val");
        st->execute();
        
        ok = check(st->getColumnCount() == 2, "UPDATE RETURNING column count") && ok;
        
        rows = 0;
        while (st->fetch())
        {
            int id = st->getInt32(0);
            std::string val = st->getString(1);
            std::cout << "    Fetched: id=" << id << ", val=" << val << "\n";
            rows++;
        }
        ok = check(rows == 4, "UPDATE RETURNING row count") && ok;

        // Test 3: Multiple-row DELETE ... RETURNING
        std::cout << "  Testing Multiple-row DELETE ... RETURNING...\n";
        st->prepare("DELETE FROM t1 WHERE id > 2 RETURNING id, val");
        st->execute();
        
        ok = check(st->getColumnCount() == 2, "DELETE RETURNING column count") && ok;
        
        rows = 0;
        while (st->fetch())
        {
            int id = st->getInt32(0);
            std::string val = st->getString(1);
            std::cout << "    Fetched: id=" << id << ", val=" << val << "\n";
            rows++;
        }
        ok = check(rows == 2, "DELETE RETURNING row count") && ok;

        tr->commit();
        db->disconnect();
    }
    catch (const std::exception& e)
    {
        std::cerr << "EXCEPTION in DmlReturningTest: " << e.what() << "\n";
        ok = false;
    }

    // Cleanup
    try 
    {
        IBPP::Database db = IBPP::DatabaseFactory(serverName, dbName, "SYSDBA", "masterkey");
        db->Connect();
        db->Drop();
    }
    catch (...) {}

    if (ok)
    {
        std::cout << "ALL DML RETURNING TESTS PASSED\n";
        return 0;
    }
    else
    {
        std::cout << "SOME DML RETURNING TESTS FAILED\n";
        return 1;
    }
}
