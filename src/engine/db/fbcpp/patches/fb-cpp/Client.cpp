#include "Client.h"
#include "Exception.h"
#include <cassert>

using namespace fbcpp;
using namespace fbcpp::impl;


void Client::shutdown()
{
	assert(isValid());

	auto dispatcher = fbRef(master->getDispatcher());
	StatusWrapper statusWrapper{*this};

	dispatcher->shutdown(&statusWrapper, 0, fb_shutrsn_app_stopped);

	master = nullptr;
}
